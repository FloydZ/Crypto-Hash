DESSetKeyEnc proc pKey:PTR BYTE
	lea eax,KeyTab
	xi = 0
	rept 8
		%pxor mm&@CatStr(%xi),mm&@CatStr(%xi)
		;xi = xi + 1
		xi = (xi + 1) and 7
	endm
	;xi = 0
	rept 8
		%movq [eax+xi*8],mm&@CatStr(%xi)
		xi = xi + 1
	endm
	rept 8
		%movq [eax+xi*8],mm&@CatStr(%(xi-8))
		xi = xi + 1
	endm
	invoke DESSetKey,pKey
	ret
DESSetKeyEnc endp

