DESEncrypt proc pBlockIn:PTR BYTE,pBlockOut:PTR BYTE
	mov ecx,[esp][1*4];pBlockIn
	push ebp
	push esi
	push edi
	push ebx
	l equ <eax>
	r equ <edx>
	mov l,[ecx][0]
	mov r,[ecx][4]
	bswap l
	bswap r
	IPERM l,r
	xi = 0
	repeat 8
		mov ebx,r
		ror ebx,4
		xor ebx,[KeyTab+xi*4*4+0*4]
		and ebx,00111111001111110011111100111111b  
		movzx edi,bl
